for k in range(10): # Com um parâmetro começa sempre do 0 (para o método range)
    print(k)
for k in range(1, 4): # Dessa forma damos um parâmetro para o início e para o fim (para o método range)
    print(k)
for x in "abacate": # Podemos usar o for para percorrermos uma string caracter a caracter
    print(x)
for x in [42, "abacate", 3.14]: # Também podemos utilizar o for para percorrermos os elementos de um vetor
    print(x)